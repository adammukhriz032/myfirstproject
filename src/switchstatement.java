public class switchstatement {
    public static void main (String args[]){
        int umur = 2;
        switch(umur){
            case 0 : 
                System.out.println("adik saya boleh meniarap");
                break;
            case 1 :
                System.out.println("adik saya boleh bercakap");
                break;
            case 2 :
                System.out.println("adik saya boleh berjalan");
                break;

        }
    }
}
